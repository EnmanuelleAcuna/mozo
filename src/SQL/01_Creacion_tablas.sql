	@estado varchar(10),
	@tipoSeguimiento varchar(10),
	@fechaInicio Datetime,
	@fechaFin DateTime
AS
BEGIN
	SELECT  A.LLP_Id AS IdAcuerdo, A.Titulo, A.NumeroVersion, A.Asunto, A.UrlAcuerdoFirmado, E.Nombre AS Estado, 
			SUBSTRING(  (SELECT ',' + U.Nombre AS [text()]
						 FROM SGJD_ACU_TAB_UNIDADES_EJECUCION_ACUERDO UA
						 INNER JOIN SGJD_ADM_TAB_UNIDADES U ON UA.LLF_IdUnidad = U.LLP_Id
						 WHERE UA.LLF_IdAcuerdo = A.LLP_Id
						 ORDER BY UA.LLF_IdUnidad
