	AND A.TipoSeguimiento = CASE @tipoSeguimiento WHEN '' THEN A.TipoSeguimiento ELSE @tipoSeguimiento END 
	AND A.FechaCreacion >= CASE @fechaInicio WHEN '19900101' THEN A.FechaCreacion ELSE  @fechaInicio END
