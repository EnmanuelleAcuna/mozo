	SELECT	Acta.LLP_Id AS IdActa, Acta.LLF_IdSesion AS IdSesion, Sesion.Numero AS 'NumeroSesion', Sesion.FechaHora AS 'FechaSesion', TipoSesion.Descripcion AS 'TipoSesion',
