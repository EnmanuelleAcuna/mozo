			A.FechaCreacion, TipoSeguimiento =
											CASE A.TipoSeguimiento 
											WHEN '0' THEN 'Sin seguimiento'
											WHEN '1' THEN 'Con seguimiento'
											WHEN '2' THEN 'Seguimiento permanente'
											ELSE 'Todas'
											END
	FROM SGJD_ACU_TAB_ACUERDOS A
	INNER JOIN SGJD_ADM_TAB_ESTADOS E ON E.LLP_Id = A.LLF_IdEstado
