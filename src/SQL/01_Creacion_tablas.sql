			--OR Capitulo.Titulo LIKE '%' + @Palabra + '%' OR Capitulo.Contenido LIKE '%' + @Palabra + '%'
			--OR Articulo.Titulo LIKE '%' + @Palabra + '%' OR Articulo.Contenido LIKE '%' + @Palabra + '%' OR Articulo.Observacion LIKE '%' + @Palabra + '%'
	ORDER BY Sesion.LLP_Id DESC
