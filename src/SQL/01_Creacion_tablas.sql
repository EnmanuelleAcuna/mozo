CREATE PROCEDURE [dbo].[SGJD_ACT_PRO_OBTENER_INFORME_ASISTENCIA_SESION_DIRECTORES]
	@Usuario varchar(100),
	@Anno varchar(10) 
AS
BEGIN
	SELECT YEAR(S.FechaHora) AS Anno, U.LLP_Id AS IdUsuario, U.Nombre AS NombreUsuario, COUNT(S.FechaHora) As CantidadSesiones,
	SUM(CASE WHEN A.Tipo = 'Presente' THEN 1 ELSE 0 END) AS Presente,
	SUM(CASE WHEN A.Tipo = 'No presente' THEN 1 ELSE 0 END) AS NoPresente
	FROM SGJD_ACT_TAB_ASISTENTES_SESION A
	INNER JOIN SGJD_ACT_TAB_SESIONES S ON S.LLP_Id = A.LLF_IdSesion
	INNER JOIN  SGJD_ADM_TAB_USUARIOS U ON U.LLP_Id = A.LLF_IdUsuario
	WHERE A.LLF_IdUsuario = CASE @Usuario WHEN '' THEN A.LLF_IdUsuario ELSE @Usuario END
	AND YEAR(S.FechaHora) = CASE @Anno WHEN '' THEN YEAR(S.FechaHora) ELSE @Anno END --2020 --@Anno
	GROUP BY YEAR(S.FechaHora), U.Nombre, U.LLP_Id
	ORDER BY YEAR(S.FechaHora) ASC
END
