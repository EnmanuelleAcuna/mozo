	SUM(CASE WHEN E.CodigoEstado = 'ACU-EE' THEN 1 ELSE 0 END) AS AcuerdosProceso,
	SUM(CASE WHEN E.CodigoEstado = 'ACU-EJEC' THEN 1 ELSE 0 END) AS AcuerdosCumplidos
	FROM SGJD_ACU_TAB_ACUERDOS A
	INNER JOIN SGJD_ADM_TAB_ESTADOS E ON E.LLP_Id = A.LLF_IdEstado
	WHERE E.CodigoEstado IN ('ACU-NOTI','ACU-EE','ACU-EJEC','ACU-REV')
	GROUP BY YEAR(A.FechaNotificacion)
	ORDER BY YEAR(A.FechaNotificacion) ASC 
END
